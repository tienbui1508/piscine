int	ft_sqrt(int nb)
{
	int	sqrt;

	sqrt = 0;
	if (nb <= 0)
		return (0);
	else
	{
		while (sqrt * sqrt < nb)
			sqrt++;
		if (sqrt * sqrt > nb)
			sqrt = 0;
		return (sqrt);
	}
}

