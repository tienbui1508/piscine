#include <unistd.h>

void	print_char(char c)
{
	write(1, &c, 1);
}

int	main(int argc, char *argv[])
{
	int	i;

	i = 0;
	while (argv[0][i] != '\0' && argc)
	{
		print_char(argv[0][i]);
		i++;
	}
	print_char('\n');
	return (0);
}
